/**
 * @author Jagan Reddy
 *
 */
public class Employee {
	
	private int employeeId;
	private char gender;
	private float allowances;
	private double basicSalary;
	
	
	/**Constructor
	 * @param employeeId
	 */
	public Employee(int employeeId) {
		super();
		this.employeeId = employeeId;
	}


	public char getGender() {
		return gender;
	}


	public void setGender(char gender) {
		this.gender = gender;
	}


	public float getAllowances() {
		return allowances;
	}


	public void setAllowances(float allowances) {
		this.allowances = allowances;
	}


	public double getBasicSalary() {
		return basicSalary;
	}


	public void setBasicSalary(double basicSalary) {
		this.basicSalary = basicSalary;
	}
	
	
	public static void main(String[] args) {
		
		Employee employee = new Employee(1757);
		employee.setAllowances( 10.36f);
		employee.setBasicSalary(12345.67);
		employee.setGender('M');
		
		System.out.println("Employee Id : "+employee.employeeId+
				"\nEmployee Allowances : "+employee.allowances+
				"\nEmployee Basic Salary : "+employee.basicSalary+
				"\nGender : "+employee.gender);
		
	}


}
