public class Student {
	
	
	private int studentId;
	private char studentType;
		
	/**
	 * Default Constructor 
	 */
	public Student() {
		super();
		this.studentId = 10;
		this.studentType = 'F';
	}

	public int getStudentId() {
		return studentId;
	}

	public void setStudentId(int studentId) {
		this.studentId = studentId;
	}

	public char getStudentType() {
		return studentType;
	}

	public void setStudentType(char studentType) {
		this.studentType = studentType;
	}

	
	public static void main(String[] args) {
		
		Student student = new Student();
		System.out.println("Printing Default Values given by constuctor");
		System.out.println("Student ID : " +student.getStudentId() +"\nStudnt Type : "+student.getStudentType());
		
		student.setStudentId(15);
		student.setStudentType('S');
		System.out.println("Printing  Values given by Using Setters");
		System.out.println("Student ID : " +student.getStudentId() +"\nStudnt Type : "+student.getStudentType());
		
		}
		
	}
	
	
	


