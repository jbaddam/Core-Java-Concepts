public class CheckersBoard {

	/**
	 * This method prints the Checkers Board pattern
	 */
	public static void printCeckerBoard() {

		for (int i = 0; i <= 8; i++) {
			System.out.print("* ");
		}

		System.out.println();
		System.out.print(" ");

		for (int i = 0; i <= 8; i++) {
			System.out.print("* ");
		}

		System.out.println();

	}

	public static void main(String[] args) {

		for (int j = 0; j < 4; j++) {

			printCeckerBoard();

		}

	}

}