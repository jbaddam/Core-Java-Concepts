public class ForLoop {

	/*
	 * Prints the Even Numbers from 50 to 80 using For Loop
	 */
	static void printEvenNumbers() {
		for (int i = 50; i < 80; i++) {
			if (i % 2 == 0) {
				System.out.println(i);
			}
		}
	}

	public static void main(String args[]) {

		printEvenNumbers();

	}
}
